import {mkdir, readFile, writeFile} from 'node:fs/promises';
import path from 'node:path';

const [, , configPath, outputPath] = process.argv;

if (!configPath || !outputPath) {
  throw new Error(
    'usage: node generate-package-list.mjs <autolinking.json> <output.java>',
  );
}

const config = JSON.parse(await readFile(configPath, 'utf8'));
const androidDependencies = Object.entries(config.dependencies ?? {})
  .map(([name, dependency]) => ({
    name,
    android: dependency.platforms?.android,
  }))
  .filter(
    dependency =>
      dependency.android?.packageImportPath &&
      dependency.android?.packageInstance,
  )
  .sort((left, right) => left.name.localeCompare(right.name));

const imports = androidDependencies
  .map(dependency => dependency.android.packageImportPath.trim())
  .join('\n');
const instances = androidDependencies
  .map(
    dependency =>
      `      // ${dependency.name}\n      ${dependency.android.packageInstance.trim()}`,
  )
  .join(',\n');
const additionalInstances = instances ? `,\n${instances}` : '';

const source = `// Generated by react_native_android_prebuild. Do not edit.
package com.poppang.rn;

import android.app.Application;
import android.content.Context;
import android.content.res.Resources;

import com.facebook.react.ReactNativeHost;
import com.facebook.react.ReactPackage;
import com.facebook.react.shell.MainReactPackage;

import java.util.ArrayList;
import java.util.Arrays;

${imports}

public final class PopPangPackageList {
  private final Application application;
  private final ReactNativeHost reactNativeHost;

  public PopPangPackageList(Application application) {
    this.application = application;
    this.reactNativeHost = null;
  }

  private ReactNativeHost getReactNativeHost() {
    return reactNativeHost;
  }

  private Application getApplication() {
    return application;
  }

  private Resources getResources() {
    return getApplication().getResources();
  }

  private Context getApplicationContext() {
    return getApplication().getApplicationContext();
  }

  public ArrayList<ReactPackage> getPackages() {
    return new ArrayList<>(Arrays.<ReactPackage>asList(
      new MainReactPackage()${additionalInstances}
    ));
  }
}
`;

await mkdir(path.dirname(outputPath), {recursive: true});
await writeFile(outputPath, source);

process.stdout.write(
  `Generated ${outputPath} with ${androidDependencies.length} native module(s).\n`,
);
